const FIREWALL = `#!/bin/bash
# Debian 12 Firewall for Net2app VoIP/SMS
ufw allow 22/tcp    # SSH
ufw allow 5060/udp  # SIP
ufw allow 5060/tcp  # SIP TCP
ufw allow 10000:20000/udp  # RTP Media
ufw allow 5038/tcp  # Asterisk AMI
ufw allow 2775/tcp  # SMPP
ufw allow 13013/tcp # Kannel SendSMS
ufw allow 13000/tcp # Kannel Admin
ufw allow 3306/tcp  # MariaDB (local only, restrict in prod)
ufw --force enable
ufw status verbose`;

