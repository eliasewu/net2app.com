const DB_SETUP = `#!/bin/bash
# MariaDB setup for Asterisk + Net2app on Debian 12
mysql -u root << 'EOF'
CREATE DATABASE asterisk CHARACTER SET utf8 COLLATE utf8_general_ci;
CREATE DATABASE net2app CHARACTER SET utf8 COLLATE utf8_general_ci;
CREATE USER 'asterisk'@'localhost' IDENTIFIED BY 'AsteriskPass123!';
CREATE USER 'net2app'@'localhost' IDENTIFIED BY 'Net2appPass123!';
GRANT ALL PRIVILEGES ON asterisk.* TO 'asterisk'@'localhost';
GRANT ALL PRIVILEGES ON net2app.* TO 'net2app'@'localhost';
FLUSH PRIVILEGES;

USE asterisk;
-- Asterisk CDR table
CREATE TABLE IF NOT EXISTS cdr (
  id INT AUTO_INCREMENT PRIMARY KEY,
  calldate DATETIME NOT NULL DEFAULT '0000-00-00 00:00:00',
  clid VARCHAR(80) NOT NULL,
  src VARCHAR(80) NOT NULL,
  dst VARCHAR(80) NOT NULL,
  dcontext VARCHAR(80) NOT NULL,
  channel VARCHAR(80) NOT NULL,
  dstchannel VARCHAR(80) NOT NULL,
  lastapp VARCHAR(80) NOT NULL,
  lastdata VARCHAR(80) NOT NULL,
  duration INT NOT NULL DEFAULT 0,
  billsec INT NOT NULL DEFAULT 0,
  disposition VARCHAR(45) NOT NULL,
  amaflags INT NOT NULL DEFAULT 0,
  accountcode VARCHAR(20) NOT NULL,
  uniqueid VARCHAR(32) NOT NULL,
  userfield VARCHAR(255) NOT NULL,
  INDEX (calldate), INDEX (dst), INDEX (accountcode)
);
EOF`;

